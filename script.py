class Morse_code():
    def __init__(self, Text):
        self.text = Text
    def Encode(self): 
        text = ""
        for i in range(len(self.text)):
            try:
                text += Morse_Letters[self.text[i]] + " "
            except:
                text += "<Invalid-letter>"
        return text
    def Decode(self): 
        text = ""
        self.text = self.text.split(" ")
        for i in range(len(self.text)):
            try:
                text += Morse_Letters_rev[self.text[i]] + " "
            except:
                text += " <Invalid-Letter> "
        text = text.replace(" ", "")
        return text